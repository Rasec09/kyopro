from strutils import parseInt

const hon = ['2', '4', '5', '7', '9']
const pon = ['0', '1', '6', '8']

let n = stdin.readLine.parseInt

if ($n)[^1] in hon:
  echo "hon"
elif ($n)[^1] in pon:
  echo "pon"
else:
  echo "bon"
